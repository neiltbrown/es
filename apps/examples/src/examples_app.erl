-module('examples_app').

-behaviour(application).

%% Application callbacks
-export([start/2
        ,stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
    Config = [{user, [user_command_handler], [user_event_handler]}],
    es:start_es(Config),
    'examples_sup':start_link().

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

